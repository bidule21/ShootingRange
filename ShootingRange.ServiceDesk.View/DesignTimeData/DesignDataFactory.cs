    public class DesignDataFactory
    {
        private readonly Random _rand;

        private readonly LastName _lastNames;
        private readonly FirstName _firstNames;

        public DesignDataFactory()
        {
            _rand = new Random(Guid.NewGuid().GetHashCode());

            _lastNames = new LastName();
            _firstNames = new FirstName();
        }

        public Shot CreateShot()
        {
            return new Shot
            {
                PrimaryScore = _rand.Next(0, 100),
            };
        }

        public string FirstName
        {
            get { return _firstNames.GetRandom(); }
        }

        public string LastName
        {
            get { return _lastNames.GetRandom(); }
        }

        public PersonShooterViewModel CreatePersonShooter()
        {
           return new PersonShooterViewModel(Person, Shooter); 
        }

        public UiCollectionShooter CreateUiCollectionShooter()
        {
            return new UiCollectionShooter(Person, Shooter, _rand.Next(100));
        }

        public UiPerson GetUiPerson()
        {
            return new UiPerson
            {
                FirstName = _firstNames.GetRandom(),
                LastName = _lastNames.GetRandom(),
                DateOfBirth = new DateTime(_rand.Next(1920,2010), _rand.Next(1, 12), _rand.Next(1, 28)),
                HasShooters = _rand.Next(2) == 1
            };
        }

        public Person Person
        {
            get
            {
                return new Person
                {
                    FirstName = _firstNames.GetRandom(),
                    LastName = _lastNames.GetRandom(),
                    DateOfBirth = new DateTime(_rand.Next(1920, 2010), _rand.Next(1, 12), _rand.Next(1, 28)),
                };
            }
        }

        public Shooter Shooter
        {
            get
            {
                return new Shooter
                {
                    ShooterNumber = _rand.Next(100001, 999999)
                };
            }
        }

        public decimal Score
        {
            get { return _rand.Next(0, 100); }
        }
    }

